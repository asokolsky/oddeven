      stringstream in(command);
      string cmd;
      in >> cmd;
      if(cmd == "Print")
      {
        db.Print();
        continue;
      }
      if(cmd == "Find")
      {
        Date date;
        in >> date;
        db.Find(date);
        continue;
      }
